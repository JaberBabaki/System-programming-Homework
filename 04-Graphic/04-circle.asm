INCLUDE E:/karshnasi/int8086.lib
.STACK 64
.DATA
H DW 40
K DW 160
R DW 20
X DW 0
Y DW 20
P DW?
.CODE
MAIN PROC FAR
MOV AX,@DATA
MOV DS,AX
MOD_GRAF
MOV BX,1
SUB BX,R
MOV P,BX
FOR:
MOV BX,H
ADD BX,X
MOV DX,K
ADD DX,Y
SET_PIC BX,DX,2
MOV BX,H
ADD BX,Y
MOV DX,K
ADD DX,X
SET_PIC BX,DX,2
MOV DX,K
SUB DX,X
SET_PIC BX,DX,2
MOV BX,H
ADD BX,X
MOV DX,K
SUB DX,Y
SET_PIC BX,DX,2
MOV BX,H
SUB BX,X
MOV DX,K
SUB DX,Y
SET_PIC BX,DX,2
MOV BX,H
SUB BX,Y
MOV DX,K
SUB DX,X
SET_PIC BX,DX,2
MOV DX,K
ADD DX,X
SET_PIC BX,DX,2
MOV BX,H
SUB BX,X
MOV DX,K
ADD DX,Y
SET_PIC BX,DX,2
INC X
CMP P,0
JG S
MOV BX,X
SAL BX,1
INC BX
ADD BX,P
MOV P,BX
JMP J
S :
	DEC Y
	MOV BX,X
	SUB BX,Y
	SAL BX,1
	INC BX
	ADD BX,P
	MOV P,BX
J :
	MOV BX,X
	CMP BX,Y
	JNG FOR
	EXIT
	MAIN ENDP